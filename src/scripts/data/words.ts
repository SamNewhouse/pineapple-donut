/**
 * Randomly generates an item name by combining a flavor adjective
 * and a fantasy-themed noun.
 *
 * Example output: "Molten Quiver", "Spectral Diadem", "Gilded Grimoire"
 *
 * Used during item seeding for games or test environments to
 * create rich, non-repetitive catalog item names.
 *
 * @returns {string} - A two-word item name suitable for display or lore.
 */
export function generateItemName(): string {
  const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const name = itemNames[Math.floor(Math.random() * itemNames.length)];
  return `${adjective} ${name}`;
}
